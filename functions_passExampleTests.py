def area_of_rectangle(width, height):
    """
    calculates the area of a rectangle

    Input: width, height
    Outut: area
    """

    area = width * height
    return area


def perimeter_of_rectangle(width, height):
    """
    calculates the perimeter of a rectangle

    Input: width, height
    Outut: perimeter
    """

    perimeter = (2 * width) + (2 * height)
    return perimeter
